package com.example.testdemo;

public class FilterInfo {
	private String K;

	private String V;

	public FilterInfo(String k, String v) {
		K = k;
		V = v;
	}

	public String getK() {
		return K;
	}

	public void setK(String s) {
		K = s;
	}

	public String getV() {
		return V;
	}

	public void setV(String s) {
		V = s;
	}
}
